// Exercise 1: Numbers
let integerNum = 10;
let floatNum = 4.5;

console.log("Integer:", integerNum);
console.log("Float:", floatNum);
console.log("Add:", integerNum + floatNum);
console.log("Subtract:", integerNum - floatNum);
console.log("Multiply:", integerNum * floatNum);
console.log("Divide:", integerNum / floatNum);
console.log("Modulus:", integerNum % 3);
console.log("Exponent:", integerNum ** 2);

// Exercise 2: Boolean and Operators
let x = 8;
let y = 12;

let isGreater = x > y;
let isLessOrEqual = x <= y;
let isEqual = x === y;
let isNotEqual = x !== y;

console.log("x > y:", isGreater);
console.log("x <= y:", isLessOrEqual);
console.log("x === y:", isEqual);
console.log("x !== y:", isNotEqual);

let a = true;
let b = false;

console.log("a && b:", a && b);
console.log("a || b:", a || b);
console.log("!a:", !a);

// Assignment Operators
